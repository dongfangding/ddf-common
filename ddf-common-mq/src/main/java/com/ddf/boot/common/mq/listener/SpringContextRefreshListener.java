@Component
@Slf4j
public class SpringContextRefreshListener implements ApplicationListener<ContextRefreshedEvent> {

    @Autowired
    private CustomizeMessagePostProcessor customizeMessagePostProcessor;

    /**
     * Handle an application event.
     *
     * @param event the event to respond to
     */
    @Override
    public void onApplicationEvent(ContextRefreshedEvent event) {
        RabbitTemplate bean = event.getApplicationContext().getBean(RabbitTemplate.class);
        bean.addBeforePublishPostProcessors(customizeMessagePostProcessor);
    }
}
